package p1;
import java.util.*;
public class Sandwich {
	String bread;
	String filling;
	ArrayList<String> toppings;
	boolean drink;
	boolean chips;
	double price;

	public Sandwich(boolean drink, boolean chips, String bread, String filling, ArrayList<String> toppings, double price){
		this.drink = drink;
		this.chips = chips;
		this.bread = bread;
		this.filling = filling;
		this.toppings = toppings;
		this.price = price;
}



}
